#include <stdio.h>

int main() {
    int a = 5, b = 17;      // กำหนดตัวแปรจำนวนเต็ม a และ b
    float c = 8.5, d = 4.0; // กำหนดตัวแปรทศนิยม c และ d

    // 1. แสดงค่าตัวแปรทั้งหมด
    printf("a = %d, b = %d, c = %.1f, d = %.1f\n", a, b, c, d); 
    // ผลลัพธ์: a = 5, b = 17, c = 8.5, d = 4.0

    // 2. d + a
    printf("d + a = %.1f\n", d + a); 
    // 4.0 + 5 = 9.0

    // 3. a - b
    printf("a - b = %d\n", a - b); 
    // 5 - 17 = -12

    // 4. c * d
    printf("c * d = %.1f\n", c * d); 
    // 8.5 * 4.0 = 34.0

    // 5. a * c
    printf("a * c = %.1f\n", a * c); 
    // 5 * 8.5 = 42.5

    // 6. c / d
    printf("c / d = %.2f\n", c / d); 
    // 8.5 / 4.0 = 2.125

    // 7. b / c
    printf("b / c = %.2f\n", b / c); 
    // 17 / 8.5 = 2.00

    // 8. a % b (modulus)
    printf("a %% b = %d\n", a % b); 
    // 5 % 17 = 5 (เพราะ a < b)

    // 9. c % a (ต้อง cast ให้เป็น int เพื่อใช้ modulus)
    printf("c %% a = %d\n", (int)c % a); 
    // (int)8.5 = 8 → 8 % 5 = 3

    // 10. c % d (ต้อง cast เป็น int เช่นกัน)
    printf("c %% d = %d\n", (int)c % (int)d); 
    // (int)8.5 = 8, (int)4.0 = 4 → 8 % 4 = 0

    return 0;
}

/*
อภิปรายผลจากการทดลอง:
1. การใช้เครื่องหมาย +, -, *, / กับตัวแปรชนิด int และ float จะได้ผลลัพธ์ตามหลักคณิตศาสตร์ปกติ
   แต่ถ้ามี float ร่วมด้วย ผลลัพธ์จะเป็น float
2. การหาร (/) ถ้าเป็น float จะได้ค่าทศนิยม แต่ถ้าเป็น int จะตัดเศษออก
3. เครื่องหมาย % (modulus) ใช้ได้เฉพาะกับจำนวนเต็ม (int) เท่านั้น 
   หากต้องการใช้กับ float ต้องแปลงเป็น int ก่อน
4. ผลลัพธ์ของ a % b จะเป็นเศษจากการหาร a ด้วย b 
   ถ้า a < b เศษคือ a เอง
5. การ cast float → int จะตัดส่วนทศนิยมทิ้งทันทีโดยไม่ปัดเศษ
*/