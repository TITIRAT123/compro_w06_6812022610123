#include <stdio.h>

int main() {
    // -------------------------
    // โปรแกรมย่อยที่ 1: w06-02-01
    // -------------------------
    int i = 1, j = 2, k; // กำหนดค่าเริ่มต้นให้ i=1, j=2 และประกาศตัวแปร k

    printf("=== w06-02-01 ===\n");
    printf("1. i=%d, j=%d, k=undefined\n", i, j); // แสดงค่าเริ่มต้น

    k = i + j; // บรรทัดที่ 2: คำนวณ k = i + j
    printf("2. i=%d, j=%d, k=%d\n", i, j, k);

    i = i + (k * j); // บรรทัดที่ 3: i = i + (k × j)
    printf("3. i=%d, j=%d, k=%d\n", i, j, k);

    j = i / 2; // บรรทัดที่ 4: j = i / 2 (หารจำนวนเต็ม)
    printf("4. i=%d, j=%d, k=%d\n", i, j, k);

    k = i % 2; // บรรทัดที่ 5: k = i mod 2
    printf("5. i=%d, j=%d, k=%d\n", i, j, k);

    i = (j + k) * 3; // บรรทัดที่ 6: i = (j + k) × 3
    printf("6. i=%d, j=%d, k=%d\n", i, j, k);

    // -------------------------
    // โปรแกรมย่อยที่ 2: w06-02-02
    // -------------------------
    double x = 1.0, y = 2.0, z; // กำหนดค่าเริ่มต้นให้ x=1.0, y=2.0 และประกาศตัวแปร z

    printf("\n=== w06-02-02 ===\n");
    printf("1. x=%.1f, y=%.1f, z=undefined\n", x, y);

    x = y + 5.0; // บรรทัดที่ 2: x = y + 5.0
    printf("2. x=%.1f, y=%.1f, z=undefined\n", x, y);

    y = x / 2.0; // บรรทัดที่ 3: y = x / 2.0
    printf("3. x=%.1f, y=%.1f, z=undefined\n", x, y);

    y = (x * 3.0) + 4.0; // บรรทัดที่ 4: y = (x × 3.0) + 4.0
    printf("4. x=%.1f, y=%.1f, z=undefined\n", x, y);

    x = -0.5 - y; // บรรทัดที่ 5: x = -0.5 - y
    printf("5. x=%.1f, y=%.1f, z=undefined\n", x, y);

    z = x + y; // บรรทัดที่ 6: z = x + y
    printf("6. x=%.1f, y=%.1f, z=%.1f\n", x, y, z);

    return 0;
}